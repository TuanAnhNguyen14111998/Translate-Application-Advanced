// kiem tra gia tri ngon ngu truyen vao co ton tai trong langs hay khong: tham so co the truyen vao la key hoac value cua ngon ngu
function getCode (langInput) {
    
    // neu langInput rong thi tra ve false
    if (!langInput) {
        return false;
    }

    // neu co langInput thi chuyen no ve dang cac ky tu in thuong
    langInput = langInput.toLowerCase();

    // neu co ma langInput trong mang langs thi return langInput no ra - chu y langInput la key chua khong phai values trong mang langs
    if (langs[langInput]) {
        return langInput;
    }

    
    var keys = Object.keys(langs).filter(function(key) {
        if (typeof langs[key] !== 'string') {
            return false;
        }
        
        // kiem tra xem neu trong langs co gia tri nao == langInput thi tra ve gia tri la mang keys co gia tri langInput nay, no khong phai tra ve gia tri true nhu ta nghi, co le la do ham object.keys se cho ra cac keys thoa man dieu kien nao do
        return langs[key].toLowerCase() == langInput;
    });

    return keys[0] || false;

}

// kiem tra xem langInput co duoc ho tro boi mang langs
function isSupported (langInput) {
    // xet xem neu keys[0] co phan tu thi la true con khong la return false
    return Boolean(getCode(langInput));
}

